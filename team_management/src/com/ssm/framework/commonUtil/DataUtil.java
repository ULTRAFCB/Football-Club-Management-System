package com.ssm.framework.commonUtil;

public class DataUtil {
	private Object[] minutes;
	private Object[] goals;
	private Object[] assists;
	private Object[] tackles;
	private Object[] clear;
	private Object[] fouls;
	private Object[] pass;

	public Object[] getMinutes() {
		return minutes;
	}

	public void setMinutes(Object[] minutes) {
		this.minutes = minutes;
	}

	public Object[] getGoals() {
		return goals;
	}

	public void setGoals(Object[] goals) {
		this.goals = goals;
	}

	public Object[] getAssists() {
		return assists;
	}

	public void setAssists(Object[] assists) {
		this.assists = assists;
	}

	public Object[] getTackles() {
		return tackles;
	}

	public void setTackles(Object[] tackles) {
		this.tackles = tackles;
	}

	public Object[] getClear() {
		return clear;
	}

	public void setClear(Object[] clear) {
		this.clear = clear;
	}

	public Object[] getFouls() {
		return fouls;
	}

	public void setFouls(Object[] fouls) {
		this.fouls = fouls;
	}

	public Object[] getPass() {
		return pass;
	}

	public void setPass(Object[] pass) {
		this.pass = pass;
	}

}
